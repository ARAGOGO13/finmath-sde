#pragma once

// =============================================================================
// Базовый абстрактный класс для скалярных стохастических дифференциальных уравнений (СДУ)
//
// Определяет интерфейс, который должна реализовать любая модель СДУ вида:
//   dX_t = f(X_t, t) dt + g(X_t, t) dW_t
//
// где:
//   f(X_t, t) – коэффициент сноса (drift)
//   g(X_t, t) – коэффициент диффузии (diffusion)
//   W_t       – стандартное броуновское движение
//
// Наследники обязаны переопределить чисто виртуальные методы drift и diffusion.
// Метод diffusion_derivative имеет реализацию по умолчанию, возвращающую 0.0;
// он должен быть переопределён для моделей, в которых коэффициент диффузии
// зависит от состояния (например, CIR, GBM), чтобы поддерживать схему Мильштейна.
// =============================================================================

struct SDE {
    virtual double drift(double x, double t) const = 0;
    virtual double diffusion(double x, double t) const = 0;

    virtual double diffusion_derivative(double x, double t) const { return 0.0; }

    virtual ~SDE() = default;
};
