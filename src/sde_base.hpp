// =============================================================================
// Базовый абстрактный класс для скалярных стохастических дифференциальных
// уравнений (СДУ) вида: dX_t = f(X_t, t) dt + g(X_t, t) dW_t
// Все модели процессов наследуют этот интерфейс.
// =============================================================================

#pragma once

struct SDE {
    // Коэффициент сноса (drift) f(X_t, t)
    virtual double drift(double x, double t) const = 0;

    // Коэффициент диффузии (diffusion) g(X_t, t)
    virtual double diffusion(double x, double t) const = 0;

    // Производная коэффициента диффузии по состоянию: dg/dx.
    // Нужна для схемы Мильштейна. По умолчанию 0 (для процессов с постоянной
    // волатильностью, где она не требуется).
    virtual double diffusion_derivative(double x, double t) const { return 0.0; }

    virtual ~SDE() = default;
};