#pragma once
#include <vector>
#include "models/black_scholes.hpp"

// =============================================================================
// Эксперимент по численной проверке теоремы Гирсанова
//
// Теорема Гирсанова утверждает, что смена вероятностной меры (с физической P
// на риск-нейтральную Q) изменяет дрейф процесса, но не его волатильность,
// при этом математические ожидания дисконтированных выплат совпадают при
// использовании производной Радона–Никодима в качестве весовой функции.
//
// Данный класс проводит сравнительный анализ трёх способов оценки цены
// европейского колл-опциона в модели Блэка–Шоулза:
//
//   1. Аналитическая формула Блэка–Шоулза (зависит только от r, σ, T, K, S0).
//   2. Монте-Карло под риск-нейтральной мерой Q (семплирование терминального
//      значения S_T с дрейфом r).
//   3. Монте-Карло под физической мерой P (семплирование с дрейфом μ) с
//      перевзвешиванием каждого сценария производной Радона–Никодима dQ/dP.
//
// Для каждого значения физического дрейфа μ из заданной сетки вычисляются:
//   • аналитическая цена (эталон)
//   • оценка Q-MC и её стандартная ошибка
//   • оценка P-MC и её стандартная ошибка
//   • отношение дисперсий оценок Var(P-MC) / Var(Q-MC), характеризующее
//     «стоимость смены меры» (дополнительную дисперсию, вносимую весом)
//
// Результаты возвращаются в виде вектора структур Point.
// =============================================================================

class GirsanovExperiment {
public:

    // -------------------------------------------------------------------------
    // Параметры:
    //   r     – безрисковая процентная ставка
    //   sigma – волатильность актива
    //   T     – время до экспирации опциона
    //   K     – страйк опциона
    //   S0    – начальная цена базового актива
    //   N_mc  – число сценариев Монте-Карло для каждого оценщика
    // -------------------------------------------------------------------------
    GirsanovExperiment(double r, double sigma, double T,
                       double K, double S0, int N_mc)
        : r_(r), sigma_(sigma), T_(T), K_(K), S0_(S0), N_mc_(N_mc) {}

    // -------------------------------------------------------------------------
    // Структура для хранения результатов в одной точке (для одного μ)
    // -------------------------------------------------------------------------
    struct Point {
        double mu;              // физический дрейф μ
        double lambda;          // рыночная цена риска λ = (μ - r) / σ
        double bs_price;        // аналитическая цена по Блэку–Шоулзу
        double rn_price;        // оценка Монте-Карло под мерой Q
        double rn_std_err;      // стандартная ошибка оценки Q-MC
        double p_price;         // оценка Монте-Карло под мерой P с весом
        double p_std_err;       // стандартная ошибка оценки P-MC
        double variance_ratio;  // отношение дисперсий: (std_err_P / std_err_Q)²
    };

    // -------------------------------------------------------------------------
    // Для каждого значения mu из mu_grid вычисляет три оценки цены колла,
    // а также отношение дисперсий. Использует фиксированные зерна генератора
    // для воспроизводимости.
    //
    // Параметры:
    //   mu_grid   – вектор значений физического дрейфа μ
    //   base_seed – базовое зерно для генераторов случайных чисел.
    //               Для i-го элемента mu_grid Q-MC использует base_seed + i,
    //               P-MC использует base_seed + i + 1000 (разные потоки).
    //
    // Возвращает вектор структур Point с результатами для каждого μ.
    // -------------------------------------------------------------------------
    std::vector<Point> run(const std::vector<double>& mu_grid,
                           unsigned base_seed = 42) const {
        BlackScholes bs(r_, sigma_, T_, K_, S0_);
        const double analytical = bs.call_price();

        std::vector<Point> result;
        result.reserve(mu_grid.size());

        for (size_t i = 0; i < mu_grid.size(); ++i) {
            double   mu_i   = mu_grid[i];
            unsigned seed_i = base_seed + static_cast<unsigned>(i);

            auto rn = bs.price_call_mc_rn(N_mc_, seed_i);
            auto p  = bs.price_call_mc_girsanov(mu_i, N_mc_, seed_i + 1000u);

            double lambda     = (mu_i - r_) / sigma_;
            double var_ratio  = (rn.std_error > 1e-15)
                                ? (p.std_error / rn.std_error) * (p.std_error / rn.std_error)
                                : 1.0;

            result.push_back({mu_i, lambda,
                              analytical,
                              rn.price, rn.std_error,
                              p.price,  p.std_error,
                              var_ratio});
        }

        return result;
    }

private:
    double r_, sigma_, T_, K_, S0_;
    int    N_mc_;
};